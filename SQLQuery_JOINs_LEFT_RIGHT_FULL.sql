-- Para responder as duas perguntas em uma única consulta, podemos utilizar o FULL JOIN:
SELECT DISTINCT
V.NOME AS NOME_DO_VENDEDOR,
V.BAIRRO AS BAIRRO_DO_VENDEDOR,
C.BAIRRO AS BAIRRO_DO_CLIENTE,
C.NOME	 AS NOME_DO_CLIENTE
FROM TABELA_DE_CLIENTES C
FULL JOIN TABELA_DE_VENDEDORES V ON C.BAIRRO = V.BAIRRO;
